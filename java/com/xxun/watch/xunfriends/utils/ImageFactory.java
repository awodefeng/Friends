    /**
     * 图片文件转换为指定编码的字符串
     *
     * @param imgFile 图片文件
     */
    public static String file2String(File imgFile) {
        InputStream in = null;
        byte[] data = null;
        //读取图片字节数组
        try {
            in = new FileInputStream(imgFile);
            data = new byte[in.available()];
            in.read(data);
            in.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
        //对字节数组Base64编码
        String result = com.xxun.watch.xunfriends.utils.Base64.encodeBytes(data);
        return result;//返回Base64编码过的字节数组字符串
    }

